#!/usr/bin/env bash
#
# Generate ASCII benchmark charts from Hypura JSON results.
#
# Usage:
#   ./benchmarks/gen_charts.sh                    # auto-detect latest results
#   ./benchmarks/gen_charts.sh results/*.json     # specific files
#
# Output: benchmarks/CHARTS.md (committed to repo)
#
# Each machine's best result per model is used. Run benchmarks first:
#   cargo run --release -- bench --max-tokens 30 ./test-models/model.gguf

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
RESULTS_DIR="$SCRIPT_DIR/results"
OUTPUT="$SCRIPT_DIR/CHARTS.md"

# Collect input files
if [ $# -gt 0 ]; then
    FILES=("$@")
else
    FILES=("$RESULTS_DIR"/*.json)
fi

if [ ${#FILES[@]} -eq 0 ]; then
    echo "No benchmark results found in $RESULTS_DIR"
    exit 1
fi

# Extract best tok/s per model per machine using jq or python
# Use python since it's available on macOS and handles JSON well
python3 - "${FILES[@]}" <<'PYTHON' > "$OUTPUT"
import json, sys, os
from collections import defaultdict

results = []
for path in sys.argv[1:]:
    try:
        with open(path) as f:
            data = json.load(f)
        if data.get("hypura") and data["hypura"].get("tok_per_sec", 0) > 0:
            results.append(data)
    except Exception:
        continue

if not results:
    print("No valid benchmark results found.")
    sys.exit(1)

# Group by (machine, model) and keep best tok/s
best = {}
for r in results:
    hw = r["hardware"]["cpu"]
    ram = r["hardware"]["ram_gb"]
    model = r["model"]["name"]
    tps = r["hypura"]["tok_per_sec"]
    key = (hw, ram, model)
    if key not in best or tps > best[key]["hypura"]["tok_per_sec"]:
        best[key] = r

entries = sorted(best.values(), key=lambda r: -r["hypura"]["tok_per_sec"])

# Helpers
def bar(val, max_val, width=40):
    if max_val <= 0:
        return ""
    filled = int(val / max_val * width)
    return "█" * filled + "░" * (width - filled)

def short_model(name):
    name = name.replace("-instruct-v0.1", "")
    name = name.replace(".Q5_K_M", " Q5")
    name = name.replace(".Q4_K_M", " Q4")
    name = name.replace("-q4_k_m", " Q4")
    return name

def format_size(gb):
    if gb >= 10:
        return f"{gb:.0f} GB"
    return f"{gb:.1f} GB"

# --- Output ---
print("# Hypura Benchmark Charts")
print()
print("*Auto-generated by `benchmarks/gen_charts.sh`*")
print()

# Chart 1: tok/s comparison — split by fits-in-memory vs overflow
# Models need ~4 GB headroom for OS/Metal. If model > RAM - 4 GB, llama.cpp OOMs.
fits = [r for r in entries if r["model"]["size_gb"] <= r["hardware"]["ram_gb"] - 4]
overflow = [r for r in entries if r["model"]["size_gb"] > r["hardware"]["ram_gb"] - 4]

print("## Generation Speed (tok/s)")
print()
print("Higher is better.")
print()

if fits:
    print("### Models that fit in memory")
    print()
    print("```")
    max_tps = max(r["hypura"]["tok_per_sec"] for r in fits)
    for r in fits:
        model = short_model(r["model"]["name"])
        tps = r["hypura"]["tok_per_sec"]
        size = format_size(r["model"]["size_gb"])
        label = f"{model} ({size})"
        label_padded = f"{label:<35s}"
        b = bar(tps, max_tps, 30)
        print(f"  {label_padded} {b} {tps:.1f} tok/s")
    print("```")
    print()

if overflow:
    print("### Models that exceed RAM (llama.cpp = OOM)")
    print()
    print("```")
    max_tps = max(r["hypura"]["tok_per_sec"] for r in overflow)
    for r in overflow:
        model = short_model(r["model"]["name"])
        tps = r["hypura"]["tok_per_sec"]
        size = format_size(r["model"]["size_gb"])
        ram = r["hardware"]["ram_gb"]
        label = f"{model} ({size} on {ram:.0f} GB RAM)"
        label_padded = f"{label:<50s}"
        b = bar(tps, max_tps, 25)
        print(f"  {label_padded} {b} {tps:.1f} tok/s")
    print()
    print(f"  llama.cpp: OOM on all models above (cannot run at any n_gpu_layers)")
    print("```")
print()

# Chart 2: Memory placement breakdown
print("## Memory Placement")
print()
print("How model tensors are distributed across storage tiers.")
print()
print("```")

for r in entries:
    model = short_model(r["model"]["name"])
    size = r["model"]["size_gb"]
    gpu = r["placement"]["gpu_gb"]
    ram = r["placement"]["ram_gb"]
    nvme = r["placement"]["nvme_gb"]
    total = gpu + ram + nvme

    gpu_pct = gpu / total if total > 0 else 0
    ram_pct = ram / total if total > 0 else 0
    nvme_pct = nvme / total if total > 0 else 0

    w = 40
    gpu_w = max(1, int(gpu_pct * w)) if gpu > 0 else 0
    ram_w = max(1, int(ram_pct * w)) if ram > 0 else 0
    nvme_w = w - gpu_w - ram_w if nvme > 0 else 0

    bar_str = "▓" * gpu_w + "▒" * ram_w + "░" * nvme_w
    bar_str = bar_str[:w]  # clamp

    label = f"{model}"
    label_padded = f"{label:<35s}"
    detail = f"{format_size(gpu)} GPU"
    if ram > 0.01:
        detail += f" | {format_size(ram)} RAM"
    if nvme > 0.01:
        detail += f" | {format_size(nvme)} NVMe"

    print(f"  {label_padded} [{bar_str}] {detail}")

print()
print("  ▓ GPU (Metal)  ▒ RAM  ░ NVMe")
print("```")
print()

# Chart 3: Prompt eval vs generation
print("## Prompt Eval vs Generation")
print()
print("```")
for r in entries:
    model = short_model(r["model"]["name"])
    prompt_ms = r["hypura"]["prompt_eval_ms"]
    gen_tps = r["hypura"]["tok_per_sec"]
    tokens = r["hypura"]["tokens_generated"]
    # Estimate prompt tokens: ~1.3 tokens per word (typical for English)
    prompt_token_est = int(len(r["config"]["prompt"].split()) * 1.3)
    prompt_tps = prompt_token_est / (prompt_ms / 1000) if prompt_ms > 0 else 0

    label = f"{model:<35s}"
    print(f"  {label} prompt: {prompt_tps:5.1f} tok/s ({prompt_ms/1000:.1f}s)  gen: {gen_tps:.1f} tok/s")

print("```")
print()

# Chart 4: Hardware summary
machines = set()
for r in entries:
    hw = r["hardware"]
    machines.add((hw["cpu"], hw["ram_gb"], hw.get("nvme_seq_gbps", 0)))

print("## Hardware")
print()
print("```")
for cpu, ram, nvme in sorted(machines):
    print(f"  {cpu}, {ram:.0f} GB unified, {nvme:.1f} GB/s NVMe seq read")
print("```")
print()

# Footer
from datetime import datetime
print(f"*Generated {datetime.now().strftime('%Y-%m-%d')}*")
PYTHON

echo "Charts written to $OUTPUT"
